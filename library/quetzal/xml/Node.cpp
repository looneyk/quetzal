//------------------------------------------------------------------------------
// XML file io
// Node.cpp
//------------------------------------------------------------------------------

#include "Node.hpp"

using namespace std;

//------------------------------------------------------------------------------
quetzal::xml::Node::Node()
{
}

//------------------------------------------------------------------------------
istream& quetzal::xml::operator>>(istream& is, const Node& node)
{
    node.read(is);

    return is;
}

//------------------------------------------------------------------------------
ostream& quetzal::xml::operator<<(ostream& os, const Node& node)
{
    node.write(os);

    return os;
}
